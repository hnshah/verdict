#!/usr/bin/env node
/**
 * QA Report Generator
 * 
 * Generates comprehensive markdown report of all QA findings.
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

function runQACheck(scriptName) {
  try {
    const scriptPath = path.join(__dirname, scriptName);
    const output = execSync(`node ${scriptPath}`, { 
      cwd: path.dirname(__dirname),
      encoding: 'utf8'
    });
    return { success: true, output };
  } catch (e) {
    return { success: false, output: e.stdout || e.message };
  }
}

function generateReport() {
  const timestamp = new Date().toLocaleString('en-US', {
    timeZone: 'America/Los_Angeles',
    dateStyle: 'medium',
    timeStyle: 'short'
  }) + ' PT';
  
  console.log('📊 Generating QA Report...\n');
  
  // Run all QA checks
  console.log('Running checks...');
  const validatorResult = runQACheck('validate-dashboard.js');
  const scoringResult = runQACheck('check-scoring.js');
  const judgeResult = runQACheck('analyze-judge.js');
  const linksResult = runQACheck('check-links.js');
  
  // Generate report
  let report = `# Dashboard QA Report\n\n`;
  report += `**Generated:** ${timestamp}\n\n`;
  report += `---\n\n`;
  
  // Summary
  report += `## Summary\n\n`;
  
  const checks = [
    { name: 'Dashboard Validation', result: validatorResult },
    { name: 'Scoring Sanity', result: scoringResult },
    { name: 'Judge Analysis', result: judgeResult },
    { name: 'Link Checker', result: linksResult }
  ];
  
  const passedCount = checks.filter(c => c.result.success).length;
  const failedCount = checks.length - passedCount;
  
  report += `- ✅ ${passedCount}/${checks.length} checks passed\n`;
  
  if (failedCount > 0) {
    report += `- ❌ ${failedCount} check(s) failed\n`;
  }
  
  report += `\n---\n\n`;
  
  // Detailed results
  report += `## Detailed Results\n\n`;
  
  checks.forEach(check => {
    const status = check.result.success ? '✅' : '❌';
    report += `### ${status} ${check.name}\n\n`;
    
    if (check.result.success) {
      report += `All checks passed.\n\n`;
    } else {
      report += `\`\`\`\n${check.result.output}\n\`\`\`\n\n`;
    }
  });
  
  // Issues section
  const criticalIssues = [];
  const warnings = [];
  
  if (!scoringResult.success) {
    const lines = scoringResult.output.split('\n');
    const criticalSection = lines.indexOf('❌ CRITICAL ISSUES');
    if (criticalSection !== -1) {
      // Parse critical issues
      for (let i = criticalSection + 1; i < lines.length; i++) {
        if (lines[i].includes('⚠️  WARNINGS')) break;
        if (lines[i].trim().startsWith('→')) {
          criticalIssues.push(lines[i].trim().replace('→ ', ''));
        }
      }
    }
  }
  
  if (criticalIssues.length > 0) {
    report += `---\n\n`;
    report += `## Critical Issues\n\n`;
    criticalIssues.forEach((issue, i) => {
      report += `${i + 1}. ${issue}\n`;
    });
    report += `\n`;
  }
  
  // Recommendations
  report += `---\n\n`;
  report += `## Recommendations\n\n`;
  
  if (criticalIssues.length > 0) {
    report += `### Immediate Actions\n\n`;
    report += `1. Add warning banners to runs with broken scoring\n`;
    report += `2. Consider re-running vision benchmarks with different judge\n`;
    report += `3. Update judge selection guidelines\n\n`;
  }
  
  if (judgeResult.output.includes('favors helpfulness')) {
    report += `### Judge Improvements\n\n`;
    report += `- ⚠️ Current judge rewards "helpful" non-answers over correct responses\n`;
    report += `- Consider using qwen2.5:7b or qwen3-coder as judge for vision evals\n`;
    report += `- Run multi-judge validation experiment to confirm\n\n`;
  }
  
  report += `### Maintenance\n\n`;
  report += `- Run QA checks after each dashboard regeneration\n`;
  report += `- Monitor judge behavior patterns over time\n`;
  report += `- Keep link checker in CI/CD pipeline\n\n`;
  
  // Footer
  report += `---\n\n`;
  report += `*Generated by verdict QA system*\n`;
  
  return report;
}

function main() {
  const report = generateReport();
  
  // Write to file
  const outputPath = path.join(__dirname, '../QA-REPORT.md');
  fs.writeFileSync(outputPath, report, 'utf8');
  
  console.log('\n✅ Report generated!\n');
  console.log(`📄 ${outputPath}\n`);
  
  // Also print to console
  console.log(report);
}

main();
